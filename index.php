  <h3><font color="white">Устав хелперов</h3>
   <font color="white"><p style="font-family:'Open Sans', sans-serif","font-size: 15px;">
1.1 Хелпер обязан в любой ситуации оставаться адекватным.<br>
1.2 Хелпер обязан отвечать на любые вопросы от игроков, написанные в любом формате.<br>
1.3 Ответ на вопрос в репорт должен быть полным, понятным, развернутым, адекватным.<br>
1.4 Хелперу разрешается использовать команду /a (адм. чат) для важных вопросов к администрации/сообщений об нарушениях правил сервера.<br>
1.5 Право наказания за то или иное нарушение в указанное в репорте остается за хелпером.<br>
1.6 Хелперу строго запрещено просить/намекать/выпрашивать своего повышения. За нарушение данного правила хелпер будет сразу снят/понижен.<br>
1.7 Хелпер обязан вести себя грамотно.<br>
1.8 Хелпер обязан прибывать на сервере от 5 часов в сутки.<br>
1.9 Каждый хелпер обязан помогать игрокам.<br>
1.10 Основная задача хелпера - ответы на вопросы в репорт.<br>
1.11 Хелперу строго запрещается нарушение правил сервера.<br>
1.12 Право наказания хелпера за то или иное нарушение может отходить от правил хелперов и остается только за главным админстратором.<br>
1.13 Хелперу запрещено брать неактив на испытательном сроке (исключение - серьезные дела).<br>
1.14 Перед уходом в неактив, хелпер обязан подать заявку на неактив в соответствующую тему в разделе, предварительно предупредив главного администратора. Нет заявки - нет неактива.<br>
<br>
2. Правила пользования чатами:<br>
<br>
2.1 В /a /b чате, так же отвечая /ot Вы обязаны вести себя адекватно, порядочно. Вы официальное лицо сервера.<br>
2.2 Хелпер обязан соблюдать правила чата, предписанные правилами сервера.<br>
2.3. Общение с игроками только через /b чат. Исключение: /az интерьер.<br>
<br>
3. Команды хелперов:<br>
<br>
/ot [id] [текст]- ответить на репорт<br>
/a [текст] - чат администрации<br>
/re [id] - слежка за игроком<br>
/reoff - выйти из слежки<br>
/freeze [id] - заморозить игрока<br>
/unfreeze [id] - разморозить игрока<br>
/slap [id] - подкинуть игрока.<br>
/az - телепорт в админ-зону.<br>
/getwarn [id] - посмотреть действующие наказание данного игрока.<br>
/spawn - заспавниться.<br>
/admins - список адм-ции онлайн.<br>
/weapcheck - проверка на dgun.<br>
/check - просмотр статистики.<br>
<br>
4. Положение об работах/фракциях/мероприятиях:<br>
<br>
4.1 Хелперу запрещено участвовать мероприятиях, если это мешает его работоспособности.<br>
4.2 Хелперу запрещено состоять в каких-либо фракциях, организациях (только с 3-го уровня).<br>
<br>
5. Прочее:<br>
<br>
5.1 Получение хелпером двух выговоров на испытательном сроке карается снятием.<br>
5.2 Получение хелпером 3 выговоров приравнивается к снятию.<br>
5.3 Хелперу запрещено нарушать правила сервера. Наказание - выговор(исключение оскорбление, неадекватное поведение. Данные пункты караются снятием).<br>
5.4 Хелперу запрещено использовать его команды в личных целях. Наказание - на усмотрение главного админстратора.<br>
5.5 Запрещено использование программ, дающих преимущество перед игроками. Исключение: ГМ, ВХ. Наказание - снятие.<br>
5.6 Запрещено проявление неакдекватного поведения на форуме. Наказание - 1 выговор.<br>
5.7 Запрещено использовать нецензурную речь в чаты на сервере. Наказание - 2 выговора.<br>
5.8 Запрещено отвечать игрокам транслитом(пример: Privet). Наказание - 1 выговор.<br>
5.9 Хелперу запрещается вести разговоры о несправедливости его наказания.<br>
<br>
6. Сроки:<br>
<br>
6.1 Испытательный срок хелпера длится 30 дней. <br>
6.2 Для получения 2 уровня, хелпер обязан пробыть на 1 уровне(испытательный срок тоже считается 1 уровнем) 30 дней.<br>
6.3 Для получения нету определенных сроков. Точные сроки определяет главный администратор руководствуясь Вашей активностью.<br>
6.4 Каждый срок неактива взятый на испытательном сроке, прибавляется к испытательному сроку.<br>
6.5 Выговор хелперу снимается по усмотрению главного админстратора.<br>
6.6 Все взятые неактивы хелпера, прибавляются к его сроку получения следующего уровня.<br>
<br>
7. Понятия:<br>
<br>
7.1 Работоспособность - работоспособность хелпера, это то, чем он занимается. Его работа, его ответы на вопросы, помощь игрокам. Если хелпер не работает, не отыгрывает минимальный онлайн в 5 часов в сутки, то его работоспособность опускается до 0.<br>
7.2 Низкий онлайн - Под низким онлайном подразумевается то, что хелпер не отыгрывает минимальный онлайн, тобишь не отыгрывает 5 часов в сутки.<br>
7.3 Блок чата - понятие, когда игрок на определенное время не сможет пользоваться всеми видами чата, кроме СМС сообщений(/phone). Игрок видит все сообщения написанные другими игроками в чаты, но ответить не сможет.<br>
7.4 Испытательный срок - время, когда хелпер должен показать составу, что имеет нужный потенциал и достоин данного поста. Так же хелпер очень уязвим в этот период, по этому должен быть внимательным, не косячить.<br>
7.5 Приоритет - понятие, показывающее важность, первенство определенного дела.<br>
7.6 В личных целях - понятие, когда хелпер использует к примеру команды не для помощи игрокам,а для собственной выгоды.<br>
7.7 Повышение хелпера - повышение хелпера, не значит просто отсидеть определенное время на определенном уровне. Как вы заметили, в каждом повышении стоит слово "минимум", а означает это то, что если вы ничего не делаете, просто отсиживаете время, то повышение вы не получите, так как по правилам я имею права вас не повышать, ввиду вашего безделья.<br>
<br>
<br>
8. По поводу АФК хелперов:<br>
<br>
9.1 Стоять больше 600 секунд в АФК запрещено. Пошли по делам (на форум или ещё куда) - вышли с сервера.<br>
9.2 Время в АФК будет вычитаться из общего онлайна при проверке онлайна.</p>
