  <h3>Правила сервера</h3>
  <font color="white"><p style="font-family:'Open Sans', sans-serif","font-size: 15px;">
1.1 Администрация не несет ответственности за утерянное имущество аккаунта в случае скачивания вами запрещенного софта, а также модов, тематических программ,взлома и так далее.<br>
1.2 Администрация не имеет права запрашивать такую информацию как пароль от аккаунта;<br>
1.3 Администратор обязан уважительно относиться к игроку;<br>
1.4 Администратор является ООС персонажем в игровом мире;<br>
1.5 Администратор обязан соблюдать правила поведения в чате игры;<br>
1.6 Администратор вправе отказать игроку в предоставлении каких-либо услуг/одолжений, нарушающих RP режим игры (выдача xp, машин, денег и т.п.);<br>
1.7 Администратор оставляет за собой право выбирать наказание, ориентируясь на тяжесть содеянного (в пределах нормы);<br>
1.8 Администратор имеет право наказать игрока в течение 5-ти дней, после совершенного игроком нарушения [В случае подачи жалобы];<br>
1.9 Администратор не может препятствовать или мешать RP процессу;<br>
1.10 Запрещено неуважительное отношение к администрации сервера;<br>
1.11 Запрещен шантаж и клевета администрации.<br>
1.12 Игрок, который зафиксировал нарушение со стороны другого игрока имеет право подать жалобу на форум, в соответствующий раздел;<br>
1.13 Игрок,который зафиксировал нарушение со стороны администрации, имеет право подать жалобу на форум;<br>
1.14 Игрок имеет право обжаловать любое накзание администрации в течение 5-ти дней с момента наказания, обратившись на форум, в раздел «Жалобы на администрацию»;<br>
1.15 Игроку запрещено вмешиваться в работу администрации сервера;<br>
1.16 Запрещено неуважительное отношение к игрокам сервера<br>
1.17 Правила действуют для любых игроков, вне зависимости от каких-либо факторов;<br>
1.18 Запрещён мультиаккаунт (Нахождение в игре с двух или более игровых аккаунтов)<br>
1.19 Запрещено использование картинок на форуме  упоминающих о фашизме и всего что связано с этим[Свастика и прочее]<br>
<br>
<b>[<span style="color: rgb(255, 128, 0)">2</span>] Общие положения.</b><br>
<br>
2.1 Разрешено иметь до 3 игровых аккаунтов на сервере (твинк), (исключение: лидеры, администраторы);<br>
2.2 Игровой год на сервере соответствует годам в реальном времени;<br>
2.3 Место действия – США. Поэтому действия стоит отыгрывать опираясь на американскую модель поведения и манеру общения;<br>
2.4 Запрещен любой софт дающий преимущество над другими игроками;<br>
2.5 Запрещен обход игровой системы(Ограничение на цену автомобилей,домов,сим-карт,скинов);<br>
<span style="color: rgb(255, 255, 77)">2.6 </span>Запрещено использование организационных скинов вне организации[Разрешено только если скин куплен у администрации и присутствует в списке допустимых скинов]<br>
2.7 Запрещена покупка игровой валюты с посторонних ресурсов.<br>
2.8 Запрещена продажа игровой валюты другим игрокам за реальные деньги.<br>
2.9 Запрещено использование багов сервера;<br>
2.10 Запрещено поведение, нарушающее RP режим игры;<br>
2.11 Запрещены non-RP разводы игроков;<br>
2.12 Запрещена передача/продажа/обмен игрового аккаунта;<br>
2.13 Запрещен обман администрации сервера;<br>
2.14 Запрещен обход наказания(Смена ника);<br>
2.15 Запрещена продажа любого имущества за реальные деньги;<br>
2.16 Запрещена non-RP парковка (на автобазаре, дорогах и т.п.);<br>
2.17 Запрещено выдавать себя за администрацию сервера;<br>
2.18 Запрещено вставать AFK, в местах, где Вы можете помешать другим игрокам (место спавна, дорога);<br>
2.19 Запрещен уход от RP-ситуации;<br>
2.20 Игрок должен отыгрывать свою роль посредством таких команд как, /me, /do, /todo, /try;<br>
2.21 Запрещена отыгровка действий растягивающая сам процесс (пример: /me помыл машину [1/5]);<br>
2.22 Запрещен флуд командой с целью помехи игроку.<br>
2.23 Незнание правил сервера не освобождает игрока от ответственности за содеянное;<br>
2.24 Запрещено совершать действия которые противоречат правилам определённого сервера<br>
2.25 Разрешено спонсорство в конкурсе [Конкурс должен проводится в оф.группе сервера]<br>
2.26 Запрещено проводить или же участвовать в конкурсах проводимых посторонними группами Вконтакте/каналами Ютуб.<br>
2.27 Запрещено распространение постороннего ПО влияющего на процесс игры и дающего преимущество над другими игроками [Наказание: Блокировка аккаунта]<br>
